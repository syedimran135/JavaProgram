package AccessModifier;

public class Student1 {
	
	public int a = 10;
	String name = "Imran";
	
	//Public AccessModifier
	public void student1() {
		System.out.println("This is the public Name " +name);
		System.out.println("This is the public number "+a);
	}
	
	//Protected
	protected void student1p() {
		System.out.println("This is the protected Name " );
		System.out.println("This is the protected number ");
	}
	}

	
