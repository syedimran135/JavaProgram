package ConditionalStatements;

public class Multiplication {
	

		
		public void multiply(int a, int b) {
			int c = a * b;
			System.out.println("The Multiplication of 2 numbers is : "+c);
		}
		
		public void multiply(int a, int b, int c) {
			int d = a * b * c;
			System.out.println("The Multiplication of 3 numbers is : "+d);
		}
		
		public void multiply(int a, int b, int c, int d) {
			int e = a * b * c * d;
			System.out.println("The Multiplication of 4 numbers is : "+e);
		}


}
