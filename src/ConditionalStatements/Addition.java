package ConditionalStatements;

public class Addition {
	
	public void addition(int a, int b) {
		int c = a + b;
		System.out.println("The Addition of 2 numbers is : "+c);
	}
	
	public void addition(int a, int b, int c) {
		int d = a + b+ c;
		System.out.println("The Addition of 3 numbers is : "+d);
	}
	
	public void addition(int a, int b, int c, int d) {
		int e = a + b + c + d;
		System.out.println("The Addition of 4 numbers is : "+e);
	}

}
