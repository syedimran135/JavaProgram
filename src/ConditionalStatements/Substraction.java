package ConditionalStatements;

public class Substraction {
	
	public void substraction(int a, int b) {
		int c = a - b;
		System.out.println("The Substraction of 2 numbers is : "+c);
	}
	
	public void substraction(int a, int b, int c) {
		int d = a - b - c;
		System.out.println("The Substraction of 3 numbers is : "+d);
	}
	
	public void substraction(int a, int b, int c, int d) {
		int e = a - b - c - d;
		System.out.println("The Substraction of 4 numbers is : "+e);
	}

}
